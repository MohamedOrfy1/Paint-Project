    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()  )
            JOptionPane.showMessageDialog(null,"Some fileds are emptys");
        else
        {
            try{
                if(Integer.parseInt(jTextField2.getText()) < 0 || Integer.parseInt(jTextField3.getText()) < 0 || Integer.parseInt(jTextField5.getText()) < 0 || Integer.parseInt(jTextField6.getText()) < 0  )
                {
                    JOptionPane.showMessageDialog(null,"Please enter positive values");
                }
                else{

                    Point point1 = new Point(Integer.parseInt(jTextField2.getText()) , Integer.parseInt(jTextField3.getText()));
                    Point point2 = new Point(Integer.parseInt(jTextField5.getText()) , Integer.parseInt(jTextField6.getText()));
                    l = new LineSegment(point1, point2);
                    l.setColor(c1);
                    LN++;
                    myDrawingPanel p1 = p.getPanel1();
                    p1.addShape(l);
                    p.addToCombo(l.toString());
                    p1.refresh(null);
                    this.setVisible(false);
                    p.setVisible(true);
                }
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null,"Please enter valid values!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

