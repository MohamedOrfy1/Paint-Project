    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Some fileds are emptys");
        else
        {
            try{
                if(Integer.parseInt(jTextField1.getText()) < 0 || Integer.parseInt(jTextField2.getText()) < 0 || Integer.parseInt(jTextField3.getText()) < 0 || Integer.parseInt(jTextField4.getText()) < 0 )
                {
                    JOptionPane.showMessageDialog(null,"Please enter positive values");
                }
                else{

                    Point point = new Point(Integer.parseInt(jTextField1.getText()) , Integer.parseInt(jTextField2.getText()));
                    Rectangle r = new Rectangle(point,Integer.parseInt(jTextField3.getText()),Integer.parseInt(jTextField4.getText()));
                    r.setColor(c1);
                    r.setFillColor(c2);
                    RN++;
                     myDrawingPanel p1 = p.getPanel1();
                    p1.addShape(r);
                    p.addToCombo(r.toString());
                    c1=Color.BLACK;
                    c2=Color.BLACK;
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    p1.refresh(null);
                    this.setVisible(false);
                    p.setVisible(true);
                }
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(null,"Please enter valid values!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

